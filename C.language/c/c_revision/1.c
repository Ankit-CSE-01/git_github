#include <stdio.h>

int main() {
    // Basic data types
    int age = 25;                   // Integer
    float price = 99.99;            // Floating point
    char grade = 'A';               // Character
    double pi = 3.1415926535;       // Double precision
    
    printf("Age: %d\n", age);
    printf("Price: %.2f\n", price);
    printf("Grade: %c\n", grade);
    printf("Pi: %.10f\n", pi);
    
    return 0;
}