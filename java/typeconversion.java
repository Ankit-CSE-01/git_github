public class typeconversion {
    public static void main(String[] args) {
        int a = 10;
        double b = 20.5;
        double c = (double)a;
        int d = (int) b;
        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);
        System.out.println("Value of c is: "+c);
        System.out.println("Value of d is: "+d);
    }
}
